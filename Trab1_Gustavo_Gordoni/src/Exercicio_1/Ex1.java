package Exercicio_1;

public class Ex1{
    
    private double n1, n2, n3, resp;
    
    public double calcular (double n1, double n2, double n3){   
  
        this.n1 = n1;
        this.n2 = n2;
        this.n3 = n3;
        
        this.resp = this.n1 * this.n2 * this.n3;
        
        return this.resp;
    }
  
}// Fecha chave